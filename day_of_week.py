day = int(input("Day (0-6)? "))

if day == 0:
    print("\nSunday")

elif day == 1:
    print("\nMonday")

elif day == 2:
    print("\nTuesday")

elif day == 3:
    print("\nWednesday")

elif day == 4:
    print("\nThursday")

elif day == 5:
    print("\nFriday")

elif day == 6:
    print("\nSaturday")

else:
    print("\nPlease enter weekday number between 0-6.")